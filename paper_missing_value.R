#1.1缺失值的分类
#缺失值从数据分布上可被分为三类：
#完全随机缺失(missing completely at random,MCAR):某一变量缺失值不依赖于其他任何原因的完全随机缺失
#随机缺失(missing at random,MAR):某一变量的缺失与其他变量相关但与该变量本身的数值不相关
#非随机缺失(missing not at random,NMAR):某一变量的缺失和该变量本身的数值相关
























